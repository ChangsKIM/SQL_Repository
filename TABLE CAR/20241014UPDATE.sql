-- 실습문제
CREATE TABLE CAR(
    CAR_ID CHAR(10),
    CAR_NAME VARCHAR2(30),
    CAR_MAKER VARCHAR2(30),
    CAR_MAKE_YEAR NUMBER(4),
    CAR_PRICE NUMBER(5)
);

SELECT * FROM CAR;



-- 자동차 테이블에서 제조사가 bmw인 자동차를 조회
SELECT * FROM CAR WHERE CAR_MAKER LIKE 'BMW';
SELECT ROWNUM, C.* FROM CAR C WHERE CAR_MAKER LIKE 'BMW'; -- 줄번호로 조회
















